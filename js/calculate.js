const calculate = {
  add(num1, num2) {
    return String(parseFloat(num1) + parseFloat(num2));
  },
  sub(num1, num2) {
    return String(parseFloat(num1) - parseFloat(num2));
  },
  multiply(num1, num2) {
    return String(parseFloat(num1) * parseFloat(num2));
  },
  divide(num1, num2) {
    return String(parseFloat(num1) / parseFloat(num2));
  }
};

